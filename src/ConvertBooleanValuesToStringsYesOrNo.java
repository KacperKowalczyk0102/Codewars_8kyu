public class ConvertBooleanValuesToStringsYesOrNo {
    /*
    Complete the method that takes a boolean value and
    return a "Yes" string for true, or a "No" string for false.
     */
    public static String boolToWord(boolean b)
    {
        if(b){
            return "Yes";
        }else{
            return "No";
        }
    }
}
